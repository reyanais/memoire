	Titre: Marguerite Yourcenar, pour toujours dans le paysage bruxellois 

Un écrin de verdure entre la Place Poelaert, identifiable à sa grande roue et le Boulevard de Waterloo, souvent très engorgé: le parc dEgmont offre une pause dans la frénésie citadine. Pour y pénétrer depuis le centre-ville, litinéraire emprunte le Passage Marguerite Yourcenar, réaménagé en 1999 pour rendre hommage à lécrivaine. 

