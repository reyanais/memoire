On gravit quelques marches, et cest comme si la conversation avec elle sengage spontanément. Sur les dalles et les murs en pierre sont gravés ses mots, des citations tirées de LRuvre au noir (1968). Quatorze extraits en tout, dont: 

CITATION SOUS FORME DE THREAD 

La route plate, bordée de peupliers, étirait devant eux un fragment du libre univers. - « L'Ruvre au noir » (1968), dans Ruvres romanesques (1982), Marguerite Yourcenar, éd. Gallimard, coll. « Bibliothèque de la Pléiade », 1991, chap. Le Grand Chemin, p. 564 

Sa route, Marguerite de Crayencour  qui choisira le nom de plume Yourcenar - lentame non loin du parc, le 8 juin 1903, au croisement de lavenue Louise et de la rue du Bailli. Elle évoque sa naissance dans Souvenirs pieux (1974), le premier romain dune trilogie autobiographique: 

CITATION SOUS FORME DE THREAD 

La maison où se passait cet événement, puisque toute naissance en est pour le père et la mère et quelques personnes qui leur tiennent de près, se trouvait située au numéro 193 de lAvenue Louise, et a disparu il y a une quinzaine dannées, dévorée par un building. - Souvenirs pieux, Monaco, Editions Alphée, 1974, p.13. 

Cette maison est dévorée dans les années 60 par la galerie commerçante Bailli-Louise. Elle est lune des nombreuses victimes de la bruxellisation: dans les années 60-70, le tissu urbain est repensé pour porter la dimension internationale et européenne de Bruxelles, ce qui donne lieu à des destructions et reconstructions anarchiques, sans plan daménagement. Lhabitat populaire et le patrimoine ancien en font les frais. Ce mouvement, cest tout ce que Marguerite Yourcenar déteste. 

